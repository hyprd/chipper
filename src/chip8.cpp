#include "chip8.h"

Chip8::Chip8(){
    memset(stack, 0, sizeof(stack));
    memset(variableRegister, 0, sizeof(variableRegister));
    memset(keypad, 0, sizeof(keypad));
    memset(display, 0, sizeof(display));
    memset(memory, 0, sizeof(memory));
    delayTimer = 0;
    soundTimer = 0;
}

Chip8::~Chip8(){}

void Chip8::Initialise(char* name) {
    LoadFontsetToMemory();
    LoadROM(name);
}

void Chip8::LoadROM(char* fname) {
    FILE* f = fopen(fname, "rb");
    fseek(f, 0, SEEK_END);
    unsigned long size = ftell(f);
    rewind(f);
    fread(memory, 1, size, f);
    fclose(f);
}

void Chip8::LoadFontsetToMemory() {
    uint8_t fontset[80] = {
        0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
        0x20, 0x60, 0x20, 0x20, 0x70, // 1
        0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
        0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
        0x90, 0x90, 0xF0, 0x10, 0x10, // 4
        0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
        0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
        0xF0, 0x10, 0x20, 0x40, 0x40, // 7
        0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
        0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
        0xF0, 0x90, 0xF0, 0x90, 0x90, // A
        0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
        0xF0, 0x80, 0x80, 0x80, 0xF0, // C
        0xE0, 0x90, 0x90, 0x90, 0xE0, // D
        0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
        0xF0, 0x80, 0xF0, 0x80, 0x80  // F
    };
    uint8_t offset = 0x50; // fontset stored 0x50 -> 0x9F typically
    for(int i = 0; i < 80; i++) {
        memory[i + offset] = fontset[i];
    }
}